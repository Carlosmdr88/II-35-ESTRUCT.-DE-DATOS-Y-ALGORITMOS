    private void btnAgregarPalabraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPalabraActionPerformed
        String palabra = txtPalabra.getText();
        pila = new Pila<>();
        cola = new Cola<>();
        if ("".equals(palabra)) {
            System.out.println(palabra);
            JOptionPane.showMessageDialog(null, "Debe ingresar una palabra");
            return;
        }
        //Mundo
        for (char c : palabra.toCharArray()) {
            cola.enqueue("" + c);
            pila.push("" + c);
        }

        ActualizarEstructuras();
        txtPalabra.setText("");
    }//GEN-LAST:event_btnAgregarPalabraActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (cola.isEmpty() && pila.isEmpty()) {
            return;
        }
        cola.dequeue();
        pila.pop();
        ActualizarEstructuras();
    }//GEN-LAST:event_btnEliminarActionPerformed

