    private void ActualizarEstructuras() {
        //Mostrar datos de estructuras
        txtPila.setText(pila.unir() + "\n" + pila.toString() + "\nCola:" + pila.nodoCima());
        txtCola.setText(cola.unir() + "\n" + cola.toString() + "\nCola:" + cola.nodoFrente());
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //dato  
        String dato = txtDato.getText();
        if ("".equals(dato)) {

            System.out.println(dato);
            JOptionPane.showMessageDialog(null, "Debe ingresar un valor");
            return;
        }
        //agregando el dato 
        cola.enqueue(dato);
        pila.push(dato);

        ActualizarEstructuras();

        txtDato.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

